<h3 >Message from the Technical Program Chairs</h3>
</div>

<div><span >
The CHI Technical Program consists of a wide variety of forums to which you can contribute. The theme of this year's conference is Crossings: crossing boundaries, crossing disciplines and crossing nations.  As the first CHI to be held in Asia, CHI 2015 is crossing into new geographic territory and we especially encourage submissions from international sources.  The CHI 2015 organizing committee has been shaped to encourage and appreciate contributions that reflect international perspectives on the crossings of people and technology, scientific and practitioner disciplines, as well as academic and business interests as we cross from the past to the future of our world.
</span></div>
