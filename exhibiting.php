  <h3 id="grand-opening-of-exhibits-and-reception">Grand Opening of Exhibits and Reception</h3>
  <p>The CHI 2015 conference reception takes place in Exhibit Hall C2-3, of the COEX Convention and Exhibition Center during the grand opening of the exhibits on Monday evening, 20 April. Live entertainment, sweet and savory offerings, and the company of friends and colleague create this memorable evening. Networking is effortless at this event attended by all conference registrants. As a special benefit to our Champion Sponsors, a complimentary exhibit booth is included in the Champion Sponsor Benefits.</p>

