<p>After you submit your Paper or Note, it will undergo a rigorous review process. This is managed by the Papers chairs and several subcommittees, each comprising a sub-committee chair and a panel of associate chairs. The review process is as follows:</p>
<ul><li>Each Paper or Note is assigned to the subcommittee chosen by the author (See:&nbsp;<a href="http://chi2015.acm.org/authors/selecting-a-subcommittee"  title="Selecting a Subcommittee">Selecting a Subcommittee</a>). The subcommittee chair will then assign the submission to an associate chair, based on their expertise.</li>
<li>The associate chair assigns each Paper or Note to at least three carefully selected reviewers. The associate chair strives to select true experts in the topic of the submission.</li>
<li>Reviewers review the Paper or Note according to the review criteria that best matches the nature of the contributions being made by the submission (See:<a href="http://chi2015.acm.org/authors/selecting-a-contribution-type"  title="Contribution Types">Contribution Types</a>), as well as the fit of that material to a Paper versus a Note (See&nbsp;<a href="http://chi2015.acm.org/authors/papers-versus-notes" >Papers versus Notes: What is the difference?</a>).</li>
<li>The associate chair completes a meta-review based on the reviews. The associate chair may also seek further reviews of the Paper or Note.</li>
<li>Authors see preliminary reviews and can respond with a rebuttal. The rebuttal offers authors an opportunity to rebut factual errors in reviews, or to answer questions asked by reviewers. Details and deadlines for the rebuttal process will be provided in the final call.</li>
<li>The associate chair (and for many papers, a secondary associate chair) compose recommendations to the committee based on all the reviews and the authorâ€™s rebuttal.</li>
<li>The committee discusses and finalizes decisions at a two-day committee meeting.</li>
<li>Confidentiality of submissions is maintained during the review process. All rejected submissions will be kept confidential in perpetuity. All submitted materials for accepted submissions will be kept confidential until the start of the conference.</li></ul>
</div>

