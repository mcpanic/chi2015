<h3 ><span ><span >alt.chi English Review Process</span></span></h3>
<div>

</div>
<p dir="ltr" ><span >Alt.chi aims to attract unusual, interesting, thought-provoking work to be presented at CHI. We're all about removing barriers to the presentation of great work, and one of those barriers involves language. We want to ensure that submissions to alt.chi are judged on the merits of the ideas, not the grammar of the writing.

To take advantage of our English review service, here's what you need to know:</span></p>

<span >
<ul ><li>To request English review, email your paper draft to <a href="mailto:alt.chi@chi2015.acm.org?Subject=%5Balt.chi%20english%20review%5D"> alt.chi@chi2015.acm.org</a>. Include the phrase "[alt.chi english review]" in the subject line -- if it doesn't include this, we don't guarantee a quick response.</li>
<li><span >Authors who want to use our English review service to send drafts <b>between 5 December and 14 December</b>. We will ask that English reviewers return reviewed drafts by 23 December in order to give the authors enough time to revise.</span></li>
<li><span >English reviewers are providing an editorial service focused on grammar and clarity -- they are not providing feedback on the content of the submission (that happens
later, during the discussion period).</span></li>
<li><span >Requests will be treated on a first-come, first-served basis. The sooner you send your request for English review, the sooner your work will be reviewed.</span></li>
<li><span >&nbsp;</span><span >After receiving an English review from our volunteers, it is up to you to alter your submission according to your reviewer’s suggestions.</span></li>
<li><span >The&nbsp;English review service does not directly affect your submission's acceptance or rejection.</span></li>
<li><span >Our English reviewers are volunteers; please be patient.</span></li>
<li><span >The English review service does not alter the submission process. You must complete your submission before the deadline, even if you have not received any feedback from the English Review Service team.</span>
<span >We hope you find this service helpful. This is a pilot program, tried only once at CHI before. Your feedback will help us decide whether and how to handle this service going
forward. </span></li></ul>
</span>

