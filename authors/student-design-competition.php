<h3 >The Competition
Structure</h3>

<p align="left" ><span>The competition
follows a three-round process. Each round focuses on communicating the team's
ideas through a different mode.</span><br>
</p>

<h4 ><span><em>Round One: Extended Abstract and
Supplementary Material</em></span></h4>
<strong><em>

</em></strong>
<p align="left" ><span><span>Teams will submit a short     paper in Extended Abstract Format (six pages maximum) summarizing their     design solution and its evolution. Teams </span><span>must</span><span> provide supplementary material in form of at most 5-minute     video. The video may illustrate how your solution fits the lives of the     users with the help of scenarios. It may also illustrate some details of     the interface and the information presented. Expert reviewers will     evaluate submissions and a maximum of 12 teams will be selected to attend     the CHI conference</span></span></p>
<p align="left" ><span>The Extended
Abstract should include:</span></p>
<ul>
 <li ><span>A description of your
     chosen design focus and proposed solution, with a summary of the
     approaches taken within your design process, the real life problems that
     you are solving, and your main claims for your proposed solution</span><span> with evaluation results</span></li>
 <li ><span>Reference to design
     principles, sources of inspiration, and HCI theory where appropriate and
     relevant</span></li>
 <li ><span>Acknowledgement of
     partial or incomplete solutions</span></li>
 <li ><span>Acknowledgement of any
     assistance drawn from outside the student team (advisors, faculty, domain
     experts, existing solutions, users, etc.)</span></li>
</ul>
<p align="left" ><span>The Supplementary </span><span>V</span><span>ideo Material may include:</span></p>
<ul>
 <li ><span>Examples of significant
     contextual data and its analysis (primary, secondary research or both)</span></li>
 <li ><span>Key creative sources of
     design inspiration (existing designs and systems)</span></li>
 <li ><span>Sketches of the evolving
     solution</span></li>
 <li ><span>Scenarios depicting how the
     solution fits in the life of users and solves problems / engages them /
     entertains them</span></li>
 <li ><span>Details of the interface
     and information design where relevant</span></li>
 <li ><span>Highlights of s</span><span>ignificant evaluation </span><span>results</span></li>
</ul>
<p align="left" ><span>All submissions
must be in English and must include title and author information, including
author affiliations. Please be sure that submissions do not contain proprietary
or confidential material and do not cite proprietary or confidential
publications. Due to tight publication schedules, revisions to the extended
abstract will not be possible. The submitted PDF version should be camera-ready
final version.</span></p>

<h4 ><span><em>Round Two: Poster</em></span></h4>
<strong><em>

</em></strong>
<p align="left" ><span><span>Accepted teams will be     expected to attend the conference to give a poster presentation outlining     their design, and discuss their proposed solution with a panel of Student     Design Competition Judges. </span></span><span>A scheduled
80-minute poster presentation event will take place during the conference.
Student teams will be expected to host their posters and discuss their
approach, design method and solutions with the Student Design Competition
Judges. The competition judges will select four teams to present their proposed
solutions orally during a scheduled presentation session named "Student
Design Competition Final". <span>Teams will be also provided space in the convention center to display posters and discuss their proposed solutions with the CHI 201</span><span>5</span><span> attendees.</span></span></p>
<p align="left" ><span>Specific
guidelines for preparing posters:</span></p>
<ul>
 <li ><span>Each poster will have a
     display space approximately 8 feet wide and 4 feet high.</span></li>
 <li ><span>The poster is expected to
     follow the International Standards Organization (ISO) poster size format
     (A0). The dimensions for A0 format are 84cm x 119cm, or approximately
     33" x 47". Either landscape or portrait orientation is
     acceptable.</span></li>
 <li ><span>Audiovisual and computing
     equipment will not be supplied. Power outlets will not be available. The
     participants may include QR codes in the poster to link to supplementary
     material online (such as scenario videos or interactive prototypes). </span></li>
</ul>
<p align="left" ><span>The poster must
include:</span></p>
<ul>
 <li ><span>The proposed solution's
     name, team name, school affiliation</span></li>
 <li ><span>The perspective taken to
     address the design challenge</span></li>
 <li ><span>A concise description of
     the proposed solution</span></li>
 <li ><span>Clear illustrations of
     key aspects of your proposed solution</span></li>
 <li ><span>Compelling, effective
     visual design</span></li>
</ul>
<p align="left" ><span>Please see
the&nbsp;</span><u><span>Information for Poster Presenters</span></u><span>.</span></p>

