<h3 >Student Design Competition Selection Process</h3>

<p align="left" ><span>Each team's short
paper submission will be reviewed by both academic and professional design and
usability experts.</span></p>
<p align="left" ><span>Round one, the
written submission, will be reviewed based on:</span></p>
<ul>
 <li ><span>Use of appropriate design
     methods such as ethnography, contextual research,
     phenomenological/autobiographical methods, secondary research, reflection,
     critique, analysis, and empirical evaluation.</span></li>
 <li ><span>Clarity and credibility
     of design focus, purpose and solution relative to the posed challenge.</span></li>
 <li ><span>Originality and quality
     of the design solution, including claims and their supporting evidence.</span></li>
 <li ><span>Innovation within the
     design process.</span></li>
 <li ><span>Quality of design
     management.</span></li>
 <li ><span>Clarity of extended
     abstract and supplementary material.</span></li>
</ul>
<p align="left" ><span>Round two, the
poster submission, will be judged based on:</span></p>
<ul>
 <li ><span>Clear communication of
     key aspects of solution</span></li>
 <li ><span>Clear communication of
     design approaches</span></li>
 <li ><span>Clear communication of
     arguments for proposed solution</span></li>
 <li ><span>Craft quality of the
     solution</span></li>
</ul>
<p align="left" ><span>Round three, the
presentation, will be judged based on:</span></p>
<ul>
 <li ><span>Clarity and organization
     of the oral presentation</span></li>
 <li ><span>Relevance and clarity of
     presentation material (slides, video, etc)</span></li>
 <li ><span>Quality of argument used
     to justify why the solution is worthy of consideration</span></li>
 <li ><span>Quality, originality and
     relevance of design solution</span></li></ul>
<div ><span><span>Submissions should not contain&nbsp;sensitive, private, or proprietary information that cannot be disclosed at publication time.&nbsp; <span>Submissions </span>should NOT be anonymous. However, confidentiality of submissions will be maintained during the review process. All rejected submissions will be kept confidential in perpetuity. All submitted materials for accepted submissions will be kept confidential until the start of the conference,<span><span><span>&nbsp;with the exception of title and author information which will be published on the website prior to the conference.&nbsp;</span></span></span></span></span><br>
</div>

<p align="left" ><span>The top four
entries to the Student Competition earn a Certificate of Recognition. The
winning entry will be recognized during the closing plenary session of the CHI
2015 conference.</span></p>
