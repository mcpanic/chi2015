<h2>Authors | Doctoral Consortium: Proposal Content</h2>

<p>This page describes the expected content for proposals submitted to the CHI 2015 Doctoral Consortium. Additional submission information can be found at the <a href="http://chi2015.acm.org/authors/doctoral-consortium">Doctoral Consortium’s CFP</a>.</p>

<h3>Doctoral Consortium Abstract</h3>

<h4>Research Situation</h4>
<p>Applicants to the Doctoral Consortium should have begun their research, but should not have completed it in its entirety. You should briefly state where you currently are in your university’s PhD program. We understand that different universities may organize their programs quite differently, so feel free to give some background if this will help you to be clear. Remember that we are seeking candidates who have an approved dissertation research topic and are carrying out their work, but who have enough work ahead of them that they can benefit from the exchanges and discussions that will take place at the Consortium.</p>

<p>Some points you may want to include are:</p>
<ul>
<li>What kind of academic program you are in</li>

<li>How you primarily identify yourself (e.g., computer scientist, ethnographer, social scientist, etc.)</li>

<li>How many years you have been in that program, and how many years you anticipate you have left before graduating</li>

<li>Whether you have completed your candidacy / research proposal stage (if required at your university)</li>

<li>A brief summary of the state of your research (e.g., what you have done vs. what you  have left to do)</li>

<li>What you hope to gain from attending the Doctoral Consortium</li>
</ul>

<h4>Context and Motivation</h4>
<p>Frame the research and motivate the problem you are studying. Describe some domain, indicate a problem in general terms, and explain why the problem is worth solving. Questions that should be answered in this section include:</p>

<ul>
<li>What is the general area being addressed?</li>

<li>How is it relevant to CHI?</li>

<li>What is the motivation for studying a particular problem?</li>

<li>Why will anyone care when the problem is solved?</li>
</ul>

<h4>Background and Related Work</h4>
<p>Provide a brief literature review to provide enough background to (a) give sufficient knowledge about what others have done, and (b) show how your work will build upon this prior work. Highlight only the key literature here; a full review is not required. Questions that should be answered in this section include:</p>

<ul>
<li>What have others done in this area? What discipline(s) have considered this area?</li>

<li>Do you have sufficient knowledge of the relevant literature necessary to do the proposed work?</li>

<li>How does the proposed work fit within and extend what has been done before?</li>
</ul>

<h4>Thesis or Problem Statement</h4>
<p>Provide a concise statement of your thesis or a problem statement. This should be the highest-level problem or goal that you plan to address. It may be posed as a hypothesis, proposition, or conjecture, and is often followed by a brief list of specific problems and sub-problems that need to be solved to satisfy the hypothesis or thesis. Problems should be stated unambiguously and must be worthy of a PhD thesis. Questions that should be answered after by this section include:</p>
<ul>
<li>Have you succinctly identified the thesis or problem(s) being addressed?

<li>Is this problem worthy of a CHI PhD thesis?
</ul>

<h4>Research Goals and Methods</h4>
<p>Translate the problem you are addressing into research goals and corresponding methods. Each goal should briefly indicate how you are going to solve the problem by describing the research method(s) you will use. Goals should be operational; i.e., if you later claim to achieve your goal, you should be able to match your solution against the goal statement. Then describe what contributions you expect to make if you satisfy these goals.</p>

<p>We cannot overstate how important it is to have clear goals. When problems, goals, methods and contributions are not clearly stated, readers will be unable to evaluate your solutions. Questions that should be answered by this section include:</p>
<ul>
<li>What are the specific goals being pursued?

<li>Do these goals actually help solve some or all of the stated problem(s)?

<li>Have you stated how you will achieve each goal (i.e., the method)?

<li>Are the goals actionable, i.e., will we know when a goal is actually attained?
</ul>

<h4>Dissertation Status</h4>
<p>Clearly state what you have done and what you have left to do. Summarize the most important findings thus far, and make it clear how these findings match and inform your original problems and goals. Include a short argument as to why these findings are important. Include references and brief descriptions to key publications (if any) arising from your thesis work. State how much of your actual thesis document is written, and what form it is in (e.g., outline, rough draft, etc.)</p>

<p>In addition to describing your current status (as of submission), please also include a paragraph about your future plans, including the research activities remaining, how much time you expect these to take, and what sorts of assistance you hope to obtain through your participation in the Consortium.</p>

<h4>Expected Contributions</h4>
Finally, connect your research approach back to the problem statement. This should be a short section that conveys what you anticipate as results or outcomes from your dissertation project and how it will contribute to the HCI research community.

<h4>References</h4>
<p>Include a list of any references sited.</p>

<p><em>Note: You may prefer to combine your Thesis Statement, Research Goals and Methods  and Expected Contributions into a single section. That is fine, as long as you clearly address the questions raised by each.</em></p>

<p><em>Note: Ensure the formatting of the Abstract conforms to the Extended Abstracts format posted on the conference web site.</em></p>



<h3>Doctoral Consortium Appendices</h3>

<h4>APPENDIX 1: Statement of Expected Benefits</h4>
<p>Attach as a 1 page appendix that includes a short (one-paragraph) statement of expected benefits of participation for both yourself and the other consortium participants. Be clear about what will you contribute as well as gain.</p>

<h4>APPENDIX 2: Supervisor’s Letter of Support</h4>
<p>Attach as a second 1 page appendix a signed letter from your supervisor that supports your attendance at the Consortium. To ensure that this letter is helpful in in the selection process, please pass on these requests to your supervisor:</p>

<p><em>To supervisors of Doctoral Consortium applicants:</em> Your letter should not summarize your student’s PhD project in detail or their past achievements – that is the job of the abstract and CV. Instead please provide the following information:</p>
<ul>
<li>How long you have supervised the student and in what capacity

<li>The status of this student with respect to your institution’s PhD requirements

<li>When you expect the student to complete his/her dissertation

<li>Why attendance at this year’s Doctoral Consortium would be particularly beneficial to the student

<li>What contributions the student is likely to make to the event if invited to attend
</ul>

<h4>APPENDIX 3: Your CV</h4>
<p>Attach as a third 1 to 2 page appendix a concise version of your current Curriculum Vita. The maximum length is 2 pages; if it is longer please edit to make more concise.</p>

<p><em>Note: All material, including the Abstract and the three appendices, must be submitted as a single PDF.</em></p>

