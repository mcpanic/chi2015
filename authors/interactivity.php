<h3><span><span>Interactivity Selection Process</span></span></h3>

<p dir="ltr"><span>The CHI 2015 Interactivity forum contains Juried and Curated content which may be invited or selected from submissions. The selection process includes reviews by the Interactivity program committee, followed by jury selection of projects based on reviews, feasibility, available space at the conference and other relevant information. Our intention is to ensure that the Interactivity track represents the range of projects being undertaken across CHI communities and these projects can be presented appropriately at the conference.</span></p>
<p dir="ltr"><span><span><span>Submissions should not contain&nbsp;sensitive, private, or proprietary information that cannot be disclosed at publication time.&nbsp; <span>Submissions </span>should NOT be anonymous. However, confidentiality of submissions will be maintained during the review process. All rejected submissions will be kept confidential in perpetuity. All submitted materials for accepted submissions will be kept confidential until the start of the conference,<span><span><span>&nbsp;with the exception of title and author information which will be published on the website prior to the conference.&nbsp;</span></span></span></span></span>
</span></p>

</div>

<div>
<h3><span><span>Interactivity for Papers and Notes</span></span></h3>

<p dir="ltr"><span>This year represents a significant changes to Interactivity submissions that are related to a submitted Paper or Note.  There will be no formal association between submissions and their associated Paper or Note, and these submissions will go through the same reviewing process as stand alone submissions.

Although there will be no separate submission and review process for these submissions, we strongly encourage authors of Papers and Notes submissions to also participate in Interactivity. This is most applicable to Papers or Notes that describe novel interactive technologies or interactive experiences. Interactivity for Papers and Notes allows authors to present a hands-on demonstration of their research in a high-visibility, high-impact forum.

Authors of Papers and Notes who wish to participate in Interactivity are required to prepare a separate non-anonymised submission for the Interactivity track. The content of the submission can be adapted from the existing Paper or Note submission and the same video materials can be used.  If your Paper or Note is not accepted and you wish to withdraw your Interactivity submission, you must do so by 22 December 2014.

Interactivity submissions that are associated with a Paper or Note submission should not be anonymised.

</span></p>
</div>

<div>
<h3><span><span>Third-party material and copyright</span></span></h3>
<p dir="ltr"><span><span>It is very important that you have the rights to use all the material that is contained in your submission, including music, video, images, etc. Obtaining permissions to use video, audio, or pictures of identifiable people or proprietary content rests with the author, not the ACM or the CHI conference. If you want to use music in your video, use royalty-free material. Authors retain copyright of the material but accepted submissions will not be published or shown at the conference without a signed form permitting ACM to publish the content. This is standard procedure for ACM publications.
</span></span></p>
</div>

<div>
<h3><span><span>At the Conference</span></span></h3>
<p dir="ltr"><span>If accepted, you will be assigned a booth or space in the interactivity space or at other locations in the conference venue. Support for building on-site and moving in/out of large/heavy exhibits is only provided before the conference start and after the conference.

At CHI Interactivity you will have a space for your work, but you are responsible for bringing and setting up most of any other equipment that is required for presenting your work. We can provide help with projectors, plasma displays, etc. only when these are absolutely required to enable the Interactivity. Please provide these details using the supplement PDF.

Note that although student volunteers will be present in the Interactivity space at all times, CHI will not be able to provide anyone to run your demonstration.

