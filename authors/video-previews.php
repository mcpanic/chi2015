    <li>The first <strong>3 seconds</strong> of the video must include the <strong>title of your paper, authors list, affiliations and contact information</strong>.
    </li>
    <li>The rest of the video should communicate <strong>the topic and the message</strong> of your work (like an elevator pitch)
    </li>
    <li><strong>The video must be appropriate</strong> in terms of rights, use of language, viewpoints or imagery, and should not offend any individuals or groups at the conference or elsewhere (we reserve the right to reject videos whose content is inappropriate).
    </li>
    <li><strong>Subtitles</strong> should be included to improve accessibility to your video (recommended)
    </li>
    <li><strong>No advertisement for the conference talk</strong>. Video Previews will be archived on the abstract page of the ACM Digital Library as a short video figure to overview your work. Their diffusion therefore goes beyond the conference and should not be seen as a teaser to the presentation. Instead, Video Previews should be thought of as a video abstract to attract more readers to the paper.
    </li>
