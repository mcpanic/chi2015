<p>
<strong>Video Previews</strong> are 30-second videos that can be thought of as an elevator pitch to give a glimpse of your work, and are here to help connect your work with the community at the conference and beyond. Since their introduction at CHI 2013, Video Previews have been adopted in world first class conferences such as ACM UIST and IEEE VIS. They provide the authors with a great opportunity to publicize their work to a wider audience and to attract more readers to their paper (*).
</p>

<p>
CHI 2015 will continue the use of video previews. All authors of accepted Papers and Notes, as well as some extended abstract venues, are <strong>strongly encouraged</strong> to submit a 30 seconds video that describes their work. The Video Previews will be made available through different mediums before and during the conference to help attendees plan their daily schedules (ACM YouTube Channel, conference program application, digital proceedings). They will also be archived and embedded on the abstract page of the ACM Digital Library.
</p>

<p><i>
If you have any questions, please contact the video preview chairs at <a href="mailto:videopreviews@chi2015.acm.org">videopreviews@chi2015.acm.org</a>.
</i></p>

<p>
(*) CHI 2013 Video Previews have a record of 268,000 views since April 2013, and CHI 2014 Video Previews have about 119,000 views since April 2014 (aggregated data from the CHI website and the ACM YouTube Channel).
</p>

