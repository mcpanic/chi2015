<h3><span><span>Case Study Selection Process</span></span></h3>
<p dir="ltr"><span>Case Studies offer narratives of the challenges of an activity, the processes/techniques used, and the results achieved (good or bad), including the impact on all stakeholders, such as the user community, the sponsoring organization, and technology providers. Accepted submissions will be chosen on the merit and contribution of the report, not only on the quality of the outcome that it describes. This means that a valuable lesson learned from a poor outcome is just as acceptable as a valuable lesson learned from a good result.

Case Studies are selected by a "jury" - a panel of experts selected by and including the Case Study chairs. Each juror will take the role of Case Study Associate Chair (AC) for part of the submissions and will inform their assessment from a number of reviews by CHI reviewers. The jury will make the final decisions, led by the Case Studies chairs. Authors will receive the reviews of their submissions after the decisions are announced, and should keep in mind that the Case Studies program is a <a href="http://chi2012.acm.org/submissions.shtml#juried"><span>Juried</span></a> contribution and thus does not follow the strict peer-review process as applied to Papers or Notes. In particular, the Case Study review process does not allow authors the opportunity to submit rebuttals.

Specifically, the review criteria will be the extent to which the case study report meets the following:</span></p>
<ul>
<li>is of interest to a broad segment of the CHI community, or redefining what should be of interest</li>
<li>advances the state of the practice</li>
<li>is described in a way that others can replicate the activity in their own environment</li>
<li>makes a convincing argument that the experience is applicable beyond the specific example described</li>
<li>clearly outlines any limitations of the report as well as of the activity described</li>
</ul>

<p dir="ltr"><span>The extended abstract should contain no sensitive, private, or proprietary information that cannot be disclosed at publication time.&nbsp; <span>Submissions should NOT be anonymous. However, confidentiality of submissions will be maintained during the review process. All rejected submissions will be kept confidential in perpetuity. All submitted materials for accepted submissions will be kept confidential until the start of the conference,</span></span><span><span><span>&nbsp;with the exception of title and author information which will be published on the website prior to the conference.</span></span></span></p>
