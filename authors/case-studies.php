<h3><span><span>What is a Case Study?</span></span></h3>
<p dir="ltr"><span>Case Studies are examples of HCI practice based on real-world experiences that will be instructive and of interest to other members of the community. Case studies provide </span><span>a substantial contribution to the field</span><span> and move it forward.</span></p>
<br>
<p dir="ltr"><span>Case Studies can illustrate, explore, report, analyze, summarize, debunk, challenge, or simply describe. They might focus, for instance, on the following topics:</span></p>
<br>
<ul><li dir="ltr">
<p dir="ltr"><span>Application, critique,</span><span> or </span><span>evolution</span><span> of a </span><span>method, process, theory,</span><span> or </span><span>tool</span></p>
</li>
<li dir="ltr">
<p dir="ltr"><span>Unfolding trends</span><span> within any area of HCI Practice or specific domain</span></p>
</li>
<li dir="ltr">
<p dir="ltr"><span>Innovation </span><span>through Research or Design (disruptive or otherwise)</span></p>
</li>
<li dir="ltr">
<p dir="ltr"><span>Design </span><span>of a specific experience, discussing its rationale, any issues, and lessons learned</span></p>
</li>
<li dir="ltr">
<p dir="ltr"><span>Research </span><span>of a specific domain, user group, or experience, discussing its insights and lessons learned</span></p>
</li>
<li dir="ltr">
<p dir="ltr"><span>Management and Strategy</span><span> of research and design in organizations</span></p>
</li>
<li dir="ltr">
<p dir="ltr"><span>HCI Teaching and Learning </span><span>in education, training, or knowledge sharing.</span></p>
</li>
<li dir="ltr">
<p dir="ltr"><span>Great designs </span><span>and 'Big Ideas' - and how to make them happen</span></p>
</li>
<li dir="ltr">
<p dir="ltr"><span>Domain-specific </span><span>topics, especially lesser known domains of interest</span></p>
</li>
<li dir="ltr">
<p dir="ltr"><span>Pilot studies</span><span> preceding and informing larger-scale investigations</span></p>
</li>
<li dir="ltr">
<p dir="ltr"><span>Critical instances </span><span>that explore particular cases of interest with little concern for generalization</span></p>
</li>
<li dir="ltr">
<p dir="ltr"><span>Related works</span><span> that may have been completed at different times but do form a coherent whole</span></p>
</li>
<li dir="ltr">
<p dir="ltr"><span>Challenges</span><span> to existing notions of Research, Design, Theory, and Practice</span></p>
</li>
<li dir="ltr">
<p dir="ltr"><span>Revisiting definitions </span><span>of HCI practice</span></p>
</li></ul>

<p dir="ltr"><span>Case Studies differ from archival research papers in that Case Studies do not define themselves as part of the potentially longer term body of academic research. They might not have as extensive a literature review as archival research papers, or might not explicitly add to HCI theory within an academic school of thought. </span></p>

<p dir="ltr"><span>The key success criteria for case studies are that they must present novel and original insights, and show potential for real impact on the HCI body of knowledge and HCI practice. Case study submissions will not be constrained by traditional academic expectations, but will be judged by their significant contribution to the field of HCI as judged by an expert jury of HCI practitioners or practitioner researchers.</span></p>

<p dir="ltr"><span>Case Studies are not considered academic archival publications, but can be republished as such, as appropriate.</span></p>
