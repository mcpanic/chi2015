<h3 >Message from the Technical Program Chairs</h3>
</div>

<div><span >The CHI Technical Program consists of a wide variety of forums to which you can contribute.&nbsp;</span><span >The theme of this year's conference is Crossings: crossing boundaries, crossing disciplines and crossing nations. &nbsp;As the first CHI to be held in Asia, CHI 2015 is crossing into new geographic territory and </span><span >we especially encourage submissions from international sources</span><span >. &nbsp;The CHI 2015 organizing committee has been shaped to encourage and appreciate contributions that reflect international perspectives on the crossings of people and technology,&nbsp;scientific and practitioner disciplines, as well as academic and business interests as we cross from the past to the future of our world.</span></div>

<div><span >Please feel free to contact the&nbsp;</span><a href="mailto:technicalprogram@chi2015.acm.org" >Technical Program chairs</a><span >&nbsp;if you have any difficulties in determining which forum is most appropriate to share your ideas.</span></div>

