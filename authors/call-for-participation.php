<div>
<h3 >Call for participation</h3>
</div>

<div>Refereed and Juried venues should actively solicit submissions from Asia-based institutions to their review processes. &nbsp;Curated venues should actively invite content from HCI-relevant areas without requiring review-based selection. &nbsp;For this year's theme, that would include content related to crossings of any kind and particularly crossings from Asia-based institutions. &nbsp;</div>
<div><br>
</div>
<div>The CHI 2015 tracks use the following <a href="https://sites.google.com/site/chi2015planning/calls-for-participation/selection-processes">selection processes</a>:</div>
<div>Refereed: Papers and Notes</div>
<div>Juried:&nbsp;Works-in-Progress, Case Studies, alt.chi</div>
<div>Curated:&nbsp;Workshops, Panels, SIG meetings, Interactivity, Videos, Courses, Doctoral Consortium, Student Game, Design and Research Competitions.</div>

<div><span >The submission process is detailed in the&nbsp;</span><a href="http://chi2015.acm.org/authors/call-for-participation"  title="Call for Participation">Call for Participation</a><span >.&nbsp;</span><a href="http://chi2014.acm.org/authors/format"  title="Format">Templates</a><span >&nbsp;are available for the different submission formats. All submissions are made through the&nbsp;</span><a href="http://precisionconference.com/~sigchi"  target="_blank" title="PCS online submission site">PCS online system</a><span >.</span></div>
