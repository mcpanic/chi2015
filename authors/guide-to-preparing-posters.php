<h4>Specific guidelines for preparing posters</h4>
<ul>
    <li>Each poster will have a display space approximately 1 meter wide and 2.5 meters high.</li>
    <li>The poster is expected to follow the International Standards Organization (ISO) poster size format (A0). The dimensions for A0 format are 84cm x 119cm, or approximately 33" x 47". Poster boards will better accommodate portrait posters, but landscape orientation is also acceptable. In case of landscape orientation, please ensure your display will fit within the 1 meter wide display space (see attached rendering).</li>
    <li>Audiovisual and computing equipment will not be supplied. Power outlets will not be available. The participants may include QR codes in the poster to link to supplementary material online (such as scenario videos or interactive prototypes).</li>
    <li>Please note that posters must be secured with tape (which will be provided). Tacks, push pins, and velcro will NOT work.</li>
</ul>

<br/>

<h4>The poster must include</h4>
<ul>
    <li>The proposed solution's name, team name, school affiliation</li>
    <li>The perspective taken to address the design challenge</li>
    <li>A concise description of the proposed solution</li>
    <li>Clear illustrations of key aspects of your proposed solution</li>
    <li>Compelling, effective visual design</li>
</ul>

