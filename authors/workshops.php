  <h3>Quick Facts</h3>

    <p>Workshops will take place on 18th and/or 19th April 2015. Workshops are only open to people who have had their position paper accepted by the workshop organizers.</p>

    <p>At least one author of any accepted position paper will need to attend the workshop. ANY author attending the workshop will need to register for both the workshop and at least one day of the conference. Please verify with your Workshop Organizers how many authors per submission can attend.</p>

    <p>For questions about a specific workshop, please directly contact the organizers of this workshop.</p>


  <h3>Message from the Workshops Chairs</h3>
    <p>Workshops are a chance for members of a community with common interests to meet in the context of a focused and interactive discussion.</p>

    <p>Please consider attending a workshop, and take part in this opportunity to move a new field forward and build community. Please see the full list of offered Workshops below, and find more information on each listed Workshop's website.</p>

    <p>Each workshop should generate ideas that will give the HCI community a new, organized way of thinking about the topic or that suggest promising directions for future research. Some workshops have resulted in edited books or special issues of journals. Others have created communities that spawned new, more specialized conferences.</p>

    <p>Workshops will be held on Saturday and Sunday April 18th and 19th, 2015. A workshop may be one or two days in length. Workshops are scheduled for six working hours per day (starting at approximately 9:00 am and ending at approximately 5:00 pm), with a mid-morning break, a lunch break, and a mid-afternoon break.</p>

    <p>Workshops typically have 15 to 20 participants. Focused interaction among participants is important, so participants must have informed positions based on prior experience. If you are interested in being a workshop participant, you must submit a position paper to the organizers of that workshop. Position papers are reviewed by the workshop organizers using their own criteria, and the organizers decide on the final list of participants. Workshops are only open to people who have had their position paper accepted by the workshop organizers.</p>

    <p>If you are an accepted workshop participant, you will be provided with a registration code by your workshop organizers. All workshop participants must register for both the workshop and for at least one day of the main conference. The reason for this policy is that workshops are supposed to be a part of the CHI conference, not separate events that happen to be in the same location at the same time.</p>

