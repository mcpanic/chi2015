<h3><span><span>Preparing and Submitting&nbsp;your Workshop Package</span></span></h3>
<p dir="ltr"><span>A workshop submission package <span>must be submitted via the&nbsp;&nbsp;<a href="http://www.precisionconference.com/~sigchi" target="_blank">PCS Submission System</a>&nbsp;by&nbsp;6 October 2014, 5:00pm PDT. The proposal must have the following four components (submitted as a single PDF file)<span>:</span></span></span></p>

<ol>
&nbsp; <li>Cover Sheet. Each submission must have a cover sheet giving contact information for the primary workshop organizer. This person will serve as the main point of contact with the Workshops Chairs.
&nbsp; </li>
&nbsp; <li>Proposal. The proposal must describe the topic and rationale for the workshop, the organizers' backgrounds and a detailed plan for conducting the workshop (before, during, and after). Workshop proposals that articulate concrete plans for pre-workshop preparation and community building (e.g., through a website or other communication with participants) and/or post-workshop follow-up for the creation of tangible outcomes (e.g., poster presentation at the "spotlight on workshops" poster session, publication of a workshop report in the SIGCHI Bulletin or a similar publication, plans for a special issue of a journal) are strongly encouraged. The proposal may not exceed 3 pages and should be prepared in the Conference Extended Abstract Format (<a href="http://chi2015.acm.org/authors/format" target="_blank">http://chi2015.acm.org/authors/format</a><span>).</span></li>
&nbsp; <li>Extented Abstract. The extended abstract is a description of the workshop intended for publication in the CHI 2015 Extended Abstracts and may not exceed four pages. It must contain a summary of the workshop goals, an overview of the workshop topic, and issues to be addressed. It must&nbsp;be prepared in the Conference Extended Abstract Format (<a href="http://chi2015.acm.org/authors/format" target="_blank">http://chi2015.acm.org/authors/format</a><span>).</span>&nbsp;Note that this is the only document from the workshop that will be included in the CHI conference publications.</li>
&nbsp; <li>Call for Participation. The call for participation is a 250-word document that will be posted on the Conference site to recruit participants for the workshop. It should describe the format and goals of the workshop, the participant selection criteria, requirements for position papers (e.g., topics to address, page length), where these papers should be submitted, and state the requirement that at least one author of each accepted position paper must attend the workshop and all participants must register for the workshop and for at least one day of the conference. Upon acceptance, the workshop organizers are required to create a web page with workshop specific information. They should include the future web page url in the submission. The workshop organizers may decide to cap the number of attendees per accepted position paper.</li>
</ol>

