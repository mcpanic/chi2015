<h3><span><span>Workshop Selection Process</span></span></h3>

<p dir="ltr"><span>Workshops are a curated track and highly selective. Workshop proposals will be selected by a jury of experts who will evaluate the work based on how compelling they expect it to be to CHI attendees. While not considered archival, curated content will be represented in the ACM Digital Library. The jury will consider several factors during the selection process, including:</span></p>

<ul>
  <li>The potential for the topic of the workshop to generate stimulating discussions and useful results.</li>
  <li>The organizers' ability to demonstrate in the proposal a well-organized process and plan for the workshop that fosters interactivity.</li>
  <li>The overall balance of topics in the Workshops program and relevance to the main conference theme, Crossings.</li>
  <li>The proposed size of the workshop. For large workshops, a clear and workable plan for facilitating a lively environment for discussion for all participants will be scrutinized.</li>
  <li>If multiple submissions are received on the same or similar topics, the organizers may be encouraged to merge them.</li></ul>
<p><span><span><span>Submissions should not contain&nbsp;sensitive, private, or proprietary information that cannot be disclosed at publication time.&nbsp; <span>Submissions </span>should NOT be anonymous. However, confidentiality of submissions will be maintained during the review process. All rejected submissions will be kept confidential in perpetuity. All submitted materials for accepted submissions will be kept confidential until the start of the conference,<span><span><span>&nbsp;with the exception of title and author information which will be published on the website prior to the conference.&nbsp;</span></span></span></span></span></span><span><span><br>
</span></span></p>
