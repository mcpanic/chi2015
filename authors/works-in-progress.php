<h3><span><span>Work-in-Progress Selection Process</span></span></h3>

<p dir="ltr"><span>Work-in-Progress submissions are selected through a Juried process. The process has been designed to acknowledge the early-stage nature of the work. All submissions are considered confidential during the review process.</span></p>

<p dir="ltr"><span>Each submission will be reviewed on the following criteria:</span></p>

<ul>
  <li><b>Significance</b>: How important is the problem or question that this submission addresses? How important is the output of this work in contributing to the identified problem or question? How greatly can others benefit from this work?</li>
  <li><b>Originality</b>: How novel is the contribution? How clearly does the submission communicate the ways that it differs from and goes beyond the most relevant previous work in this area? (Note that for a Work-in-Progress submission, full literature searches are not expected, although the most relevant citations should be included.)</li>
  <li><b>Validity</b>: How appropriate are the chosen methods for the work being undertaken? How well are the submission's claims and conclusions supported by the results?</li>
  <li><b>Written Presentation</b>: How clear and understandable is the writing in the poster abstract? To what extent does the poster abstract conform to all Extended Abstract formatting requirements and the 6-page limit?</li>
  <li><b>Visual Presentation</b> (The Poster): How well does the design of the poster effectively communicate the most important facets of the work? To what degree is the design of the poster likely to draw in an audience?</li>
  <li><b>Ability of the Work to Engender Discussion</b>: To what degree will the presentation of this work stimulate interesting conversation among researchers or practitioners? To what extent will the presenters benefit from being able to discuss their work and receive feedback at this stage?</li></ul>
<p><span>The extended abstract should contain no sensitive, private, or proprietary information that cannot be disclosed at publication time.&nbsp; <span>Submissions should NOT be anonymous. However, confidentiality of submissions will be maintained during the review process. All rejected submissions will be kept confidential in perpetuity. All submitted materials for accepted submissions will be kept confidential until the start of the conference,</span></span><span><span><span>&nbsp;with the exception of title and author information which will be published on the website prior to the conference.</span></span></span></p>


