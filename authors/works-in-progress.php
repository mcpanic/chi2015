<h3><span><span>What is a Work-in-Progress</span></span></h3>
<p dir="ltr"><span>A Work-in-Progress is a concise report of recent findings or other types of innovative or thought-provoking work relevant to the CHI community. The difference between Works-in-Progress and other contribution types (e.g. main track papers and notes) is that Work-in-Progress submissions represent work that has not reach a level of completion that would warrant the full Refereed selection process. That said, appropriate submissions should make a contribution to the body of HCI knowledge, whether realized or promised. A significant benefit of a Work-in-Progress derives from the discussion between the author and conference attendees that will be fostered by the face-to-face presentation of the work.</span></p>

<p dir="ltr"><span>The Work-in-Progress category aims to attract participation from a broad range of disciplines covering a spectrum of topics and methodologies. We encourage submissions from all of CHI's communities. Please note that summaries of completed work or reduced versions of main track CHI submissions are inappropriate as a Work-in-Progress submission and will be rejected.</span></p>

<p dir="ltr"><span>Work-in-Progress abstracts appear in the CHI Extended Abstracts proceedings. Copyright is retained by the authors, and the material from them can be used as the basis for future publications as long as there are "significant" revisions from the original. For more information on the republishability of material appearing at CHI, along with links to relevant ACM policies, please see the section on <a href="http://chi2015.acm.org/authors/selection-processes#republishability">Republishability</a> on the main CHI CFP.</span></p>
<br>
</div>

<h3><span><span>Preparing and Submitting your Work-in-Progress</span></span></h3>

<p dir="ltr"><span>Work-in-Progress submissions consist of two related documents. Failure to submit a valid version of either document will result in an automatic rejection. Due to the very rapid selection process we cannot offer any extensions to the deadline. Both documents will be considered by the reviewers when assessing your submission. The two documents required are:</span></p>
<div>

</div>
<ol>
  <li>A six-page poster abstract prepared in the <a href="http://chi2015.acm.org/authors/format#extendedformat">Extended Abstracts Format</a> and submitted as a PDF file. Your poster abstract should be close to camera-ready and thoroughly copy-edited. Work-in-Progress submissions are not anonymous and should therefore include all author names, affiliations and contact information. Please note: Any submission longer than 6 pages and/or not adhering to the Extended Abstracts Format will be automatically rejected.
  </li>

  <li>An electronic version of the poster that you will present at the conference. For the review process, your poster should be reduced to one standard page in size and submitted as a PDF file. Please note that the physical posters presented at the conference may not be larger than A0 size. </li>
</ol>
<p><strong>(New for CHI 2015) Posters will be archived with the abstracts and final posters will be due at the same time as the publication-ready abstracts.&nbsp;Therefore,<span lang="EN-GB"> please ensure your poster design is as complete
as possible at the original submission.</span></strong><br>
</p>
<p dir="ltr"><span>Due to the short selection cycle, there should be very, very few changes between your submissions and the camera-ready final documents, except in rare circumstances.</span></p>

<div>
<span>Both the Extended Abstract and the poster should communicate:</span></div>
<ol>
  <li>A concise description of the work</li>
  <li>Implications of the work for the CHI community</li>
  <li>Recommendations for further investigation and/or incorporation into practice</li>
</ol>

