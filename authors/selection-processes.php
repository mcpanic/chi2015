<h2>Selection Processes</h2>
</div>

<div>
<div><span>The CHI conference employs different selection processes to apply appropriate quality assurance for each type of content that appears in the CHI Technical Program. The different selection processes and respective publication categories provide different allowances for republication of that content in other contexts. The CHI processes are consistent with the ACM policy on Categories of publications as described in the ACM Policy on Pre-Publication Evaluation.</span></div>
<div><br>
</div>
<div>CHI uses the following selection processes:</div>
<div>
<ul>
<li id="refereed"><span><b>Refereed </b>content is rigorously reviewed by members of the program committee and peer experts. The process includes an opportunity for authors to respond to referees' critiques. &nbsp;Submitters can expect to receive formal feedback from reviewers. The program committee may ask authors for specific changes as a condition of publication. Papers and Notes are refereed content.</span></li>
<li id="juried"><span><b>Juried </b>content is also reviewed by a committee in a less rigorous process that does not include an author's response or conditional acceptance. Juried content is generally not required to make the same level of lasting and significant contribution to our knowledge and understanding as refereed content. Authors who submit to juried tracks may expect to receive light feedback of up to a few paragraphs in length. The following tracks contain juried content: Works-in-Progress, Case Studies, alt.chi.</span></li>
<li id="curated"><span><b>Curated </b>content is highly selective but does not necessarily follow a reviewing process by a committee. Curated content may be selected from submissions or invited by the track chairs. &nbsp;Authors who submit to curated tracks should not expect to receive formal feedback on their submission other than the selection decision. &nbsp;The following tracks contain curated content: Videos, Special Interest Group (SIG) meetings, Workshops, Panels, Courses, Doctoral Consortium, Interactivity, Student Design, Game and Research Competitions.</span></li>
</ul>
</div>
</div>


