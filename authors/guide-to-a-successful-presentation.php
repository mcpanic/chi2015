<h3 >Checking Content Appropriateness</h3>
<table border="0" cellpadding="5" cellspacing="4" >
<tbody>
<tr>
<td  valign="top" width="258">
<p>DON'T give a presentation that will be comprehensible and interesting only to people who work in the same area as you. Please be aware that CHI is a multidisciplinary conference, with researchers and practitioners in attendance.</p>
</td>
<td  valign="top" width="500">
<p>DO ensure that even people who have little familiarity with your sub-area of HCI can understand at least the main points:</p>
<ul><li>what questions you addressed,</li>
<li>why they're important,</li>
<li>what methods you used (not necessarily the details),</li>
<li>what your main results were, and</li>
<li>why they are interesting.</li></ul>
<p>In fact, even the experts in your area don't need to understand more than these points; for the rest, they can read the paper.</p>
</td>
</tr>
<tr>
<td  valign="top" width="258">
<p>DON'T subject your audience to an "ordeal by bulleted list." Bulleted lists - especially those with large amounts of text - should be used only in exceptional cases. They are generally boring, abstract, unconvincing, and hard to read while the speaker is talking.</p>
</td>
<td  valign="top" width="500">
<p>DO aim to be finished in your allotted time:</p>
<ul><li>15-17 minutes for papers;</li>
<li>8 minutes for notes, short papers, and interactivity.</li></ul>
<p>Your audience will love and admire you for it, and you will be rewarded with a relatively deep discussion. (If the discussion lags, the Session Chair or the program chairs will ask a question to get it going again.)</p>
Note that any speaker who exceeds the allotted time &nbsp;will be interrupted mercilessly by the Session Chair, and time for questions will be reduced accordingly.</td>
</tr>
<tr>
<td  valign="top" width="258">
<p>DON'T subject your audience to an "ordeal by bulleted list." Bulleted lists - especially those with large amounts of text - should be used only in exceptional cases. They are generally boring, abstract, unconvincing, and hard to read while the speaker is talking.</p>
</td>
<td  valign="top" width="500">
<p>DO present a series of "exhibits": images, videos, system demos, diagrams, graphs, or tables. You can explain and elaborate on these exhibits while people are looking at them.&nbsp;<strong>In general, you don't need to write what you say on the slides.</strong></p>
<p>Anyone who wants to see the points you made in black and white can read your paper. Carefully preparing an exhibit can take at least 10 times as long as dashing off a bulleted list, but your audience - your research, case study, or discussion - deserve nothing less.</p>
</td>
</tr>
<tr>
<td  valign="top" width="258">
<p>DON'T use text smaller than a 28 point font. Your audience will not be able to read your slides otherwise.</p>
</td>
<td  valign="top" width="500">
<p>DO use text sparingly: Keep your points in short, concise, outline form. This will inform the viewer about the topic, and will also help you remember your key points for discussion. There is no real need to write in full sentences, as this will unnecessarily clutter your slides.</p>
<p>If you have that much text on the screen, break part of it out to another slide.</p>
</td>
</tr>
</tbody>
</table>
<h3 >Polishing the Details</h3>
<table border="0" cellpadding="5" cellspacing="4" >
<tbody>
<tr>
<td  valign="top" width="258">
<p>DON'T put material on a slide that only the people in the front rows can read.</p>
</td>
<td  valign="top" width="500">
<p>DO pay special attention to types of material that often turn out to be illegible: screen shots and complex graphics. If an exhibit like this can't be shown legibly as a whole, find a way to zoom in on individual parts of it as they are discussed.</p>
</td>
</tr>
<tr>
<td  valign="top" width="258">
<p>DON'T clutter each slide with distracting logos and superfluous information such as the title of the talk or the name and date of the conference.</p>
</td>
<td  valign="top" width="500">
<p>DO present only material that helps you to convey your points effectively. If you must include your institution's logo on each slide, make sure that it is not the most conspicuous and interesting element on any slide.</p>
</td>
</tr>
</tbody>
</table>
<h3 >Giving the Presentation</h3>
<table border="0" cellpadding="5" cellspacing="4" >
<tbody>
<tr>
<td  valign="top" width="258">
<p>DON'T risk fumbling desperately with the laptop at the beginning of your talk.</p>
</td>
<td  valign="top" width="500">
<p>DO arrive 20 minutes before your session to test the compatibility of your laptop with the projector.</p>
<p>If you bring your presentation on a CD or memory stick to present on someone else's laptop, do everything possible to maximize its portability, and test the presentation at the earliest opportunity, leaving plenty of time to fix any problems (e.g., replacing missing fonts).</p>
</td>
</tr>
<tr>
<td  valign="top">
<p>DON'T talk in such a way that only a fraction of the listeners can understand you.</p>
</td>
<td  valign="top">
<p>DO keep in mind the people in the back row who are not especially experienced in listening to English-language presentations. Native speakers of English need to avoid speaking too fast or colloquially; nonnative speakers should enunciate especially clearly so that any foreign accent does not impair comprehension.</p>
<p>DO use your microphone, even if there are not many attendees in your session. Session rooms are still enormous, and you will be on a stage. Remember that the use of a microphone does not in itself guarantee that people in the back can hear you easily: Speak up in a lively manner!</p>
</td>
</tr>
<tr>
<td  valign="top">
<p>DON'T ignore your Session Chair's time warnings.</p>
</td>
<td  valign="top">
<p>DO pay attention to the Session Chair's countdown cards. You will receive warnings at five minutes prior, one minute prior, and when time is up. If you do not stop when time is called, your Session Chair will come to the stage to start the Q&amp;A session.</p>
</td>
</tr>
<tr>
<td  valign="top">
<p>DON'T rush to cover your remaining content if you are running out of time.</p>
</td>
<td  valign="top">
<p>DO rehearse your presentation before attending CHI, and if you find yourself with a lot more content to cover at the 5-minute mark, resist the temptation to speak faster to finish. Your audience will not remember that much material!</p>
</td>
</tr>
</tbody>
</table>
<h3 >Answering Questions</h3>
<table border="0" cellpadding="5" cellspacing="4" >
<tbody>
<tr>
<td  valign="top" width="258">
<p>DON'T end your presentation with a slide that contains only an uninformative text like "Any questions?"</p>
</td>
<td  valign="top" width="500">
<p>DO conclude with a slide summarizing your main contributions, leaving it on the screen except when a question requires you to switch to another slide. (This is one of those rare cases where a bulleted list may be appropriate.) This slide will help people to think of important questions to ask - and also to be impressed by your achievements.</p>
</td>
</tr>
<tr>
<td  valign="top" width="258">
<p>DON'T use a question from the audience as a springboard to leap into the five minutes of your talk that you had to leave out because of the time limit.</p>
</td>
<td  valign="top" width="500">
<p>DO answer each question directly and concisely, without digressing into related topics. Give others a chance to ask their questions as well.</p>
</td>
</tr>
</tbody>
</table>
