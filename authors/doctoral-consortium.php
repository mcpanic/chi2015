<div>
<h3 ><span ><span >What is the Doctoral Consortium</span></span></h3>
<p dir="ltr" ><span >The Consortium has the following objectives:</span></p>

<ul >
  <li>Provide a setting where students can present their work and meet other students,</li>
  <li>Provide feedback on students' current research and guidance on future research directions,</li>
  <li>Offer each student comments and fresh perspectives on their work from researchers and students outside their own institution,</li>
  <li>Promote the development of a supportive community of scholars and a spirit of collaborative research, and</li>
  <li>Contribute to the conference goals through interaction with other researchers and conference events.</li>
</ul>
<p dir="ltr" ><span >The Consortium will be held on Saturday and Sunday, 18-19 April 2015. The consortium will be a single track two-day event with about 12-15 students attending. Applicants who are selected will receive complimentary conference registration and some travel support.</span></p>
</div>




<div>
<h3 ><span ><span >Preparing and Submitting your Doctoral Consortium Proposal</span></span></h3>
<p dir="ltr" ><span >A Doctoral Consortium proposal must be submitted via the&nbsp;&nbsp;<a href="http://www.precisionconference.com/~sigchi"  target="_blank">PCS Submission System</a>&nbsp;by&nbsp;6 October 2014, 5:00pm PDT. The proposal must have the following four components, submitted as a single PDF file. Additional details about the expected content can be found <a href="http://chi2015.acm.org/authors/doctoral-consortium-expected-proposal-content">here</a>.</span></p>

<ol >
  <li>Extended Abstract. A 4-page research description covering central aspects of your PhD work<span >. Key points include:</span></li>

<ul>
      <li>Your situation, i.e., the university doctoral program context in which your work is being conducted</li>
      <li>Context and motivation that drives your dissertation research</li>
      <li>Research objectives/goals/questions</li>
      <li>Background/literature review of key works that frames your research</li>
      <li>Hypothesis/thesis and/or problem statement</li>
      <li>Your research approach and methods, including relevant rationale</li>
      <li>Results to date and their validity</li>
      <li>Dissertation status and next steps</li>
      <li>Current and expected contributions</li>
    </ul>
  <li>Appendix 1. A one-paragraph statement of expected benefits of participation for both yourself and the other consortium participants (i.e., what will you contribute as well as gain) (max 1 page).</li>
  <li>Appendix 2. A letter of nomination from your primary dissertation advisor, that addresses the questions posed in the CHI Doctoral Consortium submission details (max 1 page).</li>
  <li>Appendix 3. A concise version of your current Curriculum Vita (1-2 pages).</li>
</ol>
<p dir="ltr" ><span >It is essential for you to ensure that your submission is complete and in accordance with the format and content guidelines for the CHI Doctoral Consortium. B</span><span >ecause the Doctoral Consortium Committee expects to receive a large number of applications, submissions that do not meet these requirements will be returned without review.</span><span ><br>
</span></p>
</div>



<div>
<h3 ><span ><span >Doctoral Consortium Selection Process</span></span></h3>
<p dir="ltr" ><span >The review and decision of acceptance will balance many factors. This includes the quality of your proposal, and where you are within your doctoral education program. Candidates who have a clearly developed idea, who are formally considered by their institution to be working on their dissertation, and who still have time to be influenced by participation in the Consortium will receive the strongest consideration. Participants in the Consortium will be selected via a curation process by the Doctoral Consortium Committee.</span></p>

<p dir="ltr" ><span >Although selection will take into account research quality, it will also include many external factors. The goal is to identify a set of students that will benefit significantly from the event and support each other in their growth. As a group, the accepted candidates will exhibit a diversity of backgrounds and topics. Your institution will be taken into account, where we are unlikely to accept more than two students from the same institution and almost certainly will not accept more than one student with the same advisor. Attendees from previous CHI or SIGCHI (e.g., CSCW, UIST) doctoral consortia are strongly discouraged from applying, and should clearly explain how they think attending a second related event would benefit them in a new way.</span></p>

<p dir="ltr" ><span >Submissions should not contain&nbsp;sensitive, private, or proprietary information that cannot be disclosed at publication time.&nbsp; <span >Submissions </span>should NOT be anonymous. However, confidentiality of submissions will be maintained during the review process. All rejected submissions will be kept confidential in perpetuity. All submitted materials for accepted submissions will be kept confidential until the start of the conference,<span ><span ><span >&nbsp;with the exception of title and author information which will be published on the website prior to the conference.&nbsp;</span></span></span> </span></p>

</div>

