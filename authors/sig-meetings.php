<h3>What is a Special Interest Groups</h3>
<p><span>Special Interest Groups (SIGs) enable conference attendees sharing a common interest to meet informally for 80 minutes of facilitated discussion during a scheduled session at the conference. SIGs offer excellent opportunities for dialog and deliberation on a specific topic. The conference provides each SIG with meeting space and advertises SIG meetings to the rest of the conference in the Extended Abstracts and Conference Program.</span></p>
<h3>Preparing and Submitting your Special Interest Groups Proposal</h3>
<p><span><span>SIG submissions <span>must be submitted via the&nbsp;&nbsp;<a href="http://www.precisionconference.com/~sigchi" target="_blank">PCS Submission System</a>&nbsp;by&nbsp;5 January 2015, 5:00pm PST. The proposal must have the following two components (submitted as a single PDF file)<span>:</span></span></span></span><span><br>
</span></p>
<ol><li>Extended Abstract. A&nbsp;4-page description of the SIG, suitable for publication in the&nbsp;<a href="http://chi2015.acm.org/authors/format#extendedformat">Extended Abstract Format</a>&nbsp;</li>
<li>Supplementary Material.&nbsp;&nbsp;</li>
<ul><li>A&nbsp;brief description of the community (or communities) to which this SIG would be of interest and why it is of interest</li>
<li>assumed attendee background</li>
<li>the approach you will use for organizing and presenting the SIG</li>
<li>an informal schedule of discussion topics</li>
<li>your plan to attract a reasonable number of attendees</li>
<li>which organizer should serve as the primary contact.</li></ul>
</ol>
<p><span>While not strictly required, we prefer that proposals be authored by a minimum of two individuals representing at least two different organizations. We also encourage prospective SIG organizers to examine examples of accepted SIG abstracts from recent CHI conferences, available for download from the ACM Digital Library.</span></p>

