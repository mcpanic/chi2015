<h4 id="user-experience-and-usability">User Experience and Usability</h4>
<p>This subcommittee is suitable for papers that contribute by extending the knowledge, approaches, practices, methods, components and tools that make technology more useful, usable and desirable. Successful papers will present results, practical approaches, tools, technologies and research methods that demonstrably advance our understanding and design capabilities for user experience and/or usability. The focus is on usability of widely used technologies. Applications targeting select user groups (e.g., accessibility) should be submitted to the Specific Applications subcommittee. Contributions will be judged substantially on the basis of their demonstrable potential for effective reuse and applicability across a range of application domains and/or design, research, or user communities.</p>
