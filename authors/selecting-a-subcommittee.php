<div >
<p>CHI 2015 anticipates around 2,000 Papers and Notes submissions. The review process needs to handle this load while also providing high-quality reviews. The organization of the CHI program committee into topical subcommittees helps achieve this by having you, the author, select the best subcommittee to review your submission.</p>
<p>The subcommittee structure empowers you to choose the appropriate community of researchers to review your research. An important thing to consider in selecting a subcommittee is that you are not describing your paper, you are instead providing information about your most important contribution and therefore the type of researcher who you feel is most qualified to review your paper.</p>
<h3 >Guidance</h3>
<p>The author decides which subcommittee reviews his or her submission. When you submit a paper or note, you will designate which subcommittee you want to handle your submission. You will see a list of subcommittees and descriptions of the topics they are covering, the name of each Subcommittee Chair, and the names of some of the Associate Chairs serving on each subcommittee. Using all of this information, it is your responsibility to select the subcommittee that best matches the expertise needed to assess your research, and that you believe will most fully appreciate your contribution to the field of HCI.</p>
<p>CHI has traditionally supported diverse and interdisciplinary work and continues to expand into new topics not previously explored. We recognize that as a result, you may find several different subcommittees which are plausible matches for aspects of your work. Hence it may be difficult to choose between subcommittees. However, for a number of reasons it will be necessary for you to select one target subcommittee, and you should strive to find the best match based on what you think is the main contribution of your submission (examples of papers that are considered good matches are linked below for each subcommittee). You can also email the Subcommittee Chairs for guidance if you are unsure.</p>
<p>Note that the scope of each subcommittee is not rigidly defined. Each has a broad mandate and most subcommittees cover a collection of different topics. Further, Subcommittee Chairs are all seasoned researchers, experienced with program committee review work, and each is committed to a process which seeks to assign each paper reviewers who are true experts in whatever the subject matter of the paper is. Subcommittee Chairs recognize that many papers, or perhaps even most papers, will not perfectly fit the definition of their subcommittee's scope. Consequently, papers will not be penalized or downgraded because they do not align perfectly with a particular subcommittee. Interdisciplinary, multi-topic, and cross-topic papers are encouraged, and will be carefully and professionally judged by all subcommittees.</p>
<p>In making a subcommittee choice you should make careful consideration of what the most central and salient contribution of your work is, even if there are several different contributions. As an example, let's say you are writing a paper about Ergonomic Business Practices for the Elderly using Novel Input Devices. Perhaps this is a very new topic. It covers a lot of ground. It's not an exact fit for any of the subcommittees, but several choices are plausible. To choose between them, you need to make a reasoned decision about the core contributions of your work. Should it be evaluated in terms of the usage context for the target user community? The novel methodology developed for your study? The system and interaction techniques you have developed? Each of these evaluation criteria may partially apply, but try to consider which is most central and which you most want to highlight for your readers. Also look at the subcommittees, the people who will serve on them, and the kind of work they have been associated with in the past. Even if there are several subcommittees that could offer fair and expert assessments of this work, go with the one that really fits the most important and novel contributions of your paper. That committee will be in the best position to offer constructive and expert review feedback on the contributions of your research.</p>
<p>Each subcommittee description also links one or two recent CHI papers that the subcommittee chairs feel are good examples of papers that fit the intent and aim of that subcommittee. Please look at these examples as a way to decide on the best subcommittee for your paper - but remember that these are just a few examples, and do not specify the full range of topics that would fit with any subcommittee. (Note: the example papers will be linked as they are selected by the chairs).</p>
