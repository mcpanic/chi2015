<h4 id="design">Design</h4>
<p>This subcommittee will focus on papers that make a contribution to the design of interactive products, services, or systems; or that advance knowledge of the human activity of design as it relates to HCI. It will cover a broad range of design approaches: participatory, user-centered, experience, and service. It will also cover a range of design practices: interaction, industrial, experience, information, architecture, visual communication, and sensorial. Finally, it will focus on design research issues such as aesthetics, values, effects (such as emotion), methods, practices, critique, and theory.</p>
<p>This subcommittee is also the home for papers related to the design of computer games.</p>
