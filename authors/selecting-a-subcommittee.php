<h4 id="understanding-people-theory-concepts-methods">Understanding People: Theory, Concepts, Methods</h4>
<p>This subcommittee will focus on papers whose primary contribution is improved understanding of people and/or interactional contexts, as applied to address HCI problems. This understanding can be derived from qualitative or quantitative research, and can be study-based or more conceptual in nature. The core contribution is likely to take the form of evolved theories, concepts or methods. These contributions will be judged in part by their extension of our basic understanding of human behavior and/or their context of activity and the practical impact this may have on HCI practice and research.</p>
