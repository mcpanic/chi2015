<h4 id="specific-application-areas">Specific Application Areas</h4>
<p>This subcommittee will focus on papers that extend the design and understanding of applications for specific application areas or domains of interest to the HCI community. Examples of potential user groups of interest include, but are not limited to: older adults, children, families, disabled people, people in developing countries, and people with perceptual, cognitive, or motor impairments. Examples of application areas include, but are not limited to: education, health, home, sustainability, ict4d, security, privacy and creativity. These contributions will be evaluated in part based on their impact on the specific application area and/or group that they address, in addition to their impact on HCI.</p>
