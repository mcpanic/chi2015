  <p>Both assistive technologies and conventional user agents can render text more accurately when the language of the document is identified. The document language needs to be verified in Adobe Acrobat Pro.</p>

  <h3>Adobe Acrobat Pro</h3>
  <p>The following instructions are for Adobe Acrobat 9 Pro. The instructions are similar for other versions of Adobe Acrobat Pro.</p>
  <ol>
    <li>Open the document in Adobe Acrobat Pro</li>
    <li>From the File menu, select "Properties..."</li>
    <li>In the "Properties" dialog, select the "Advanced" tab</li>
    <li>In the "Reading Options" field, select the default language from the "Language" combo box</li>
  </ol>
