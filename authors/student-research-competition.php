<h3><span><span>At the Conference</span></span></h3>
<p dir="ltr"><span>The first round of the competition evaluates the research during a poster presentation at CHI. The presentation will be evaluated on two dimensions, given equal weight: (1) the presentation of the research, including visual aspects of the poster and the student's verbal discussion, and (2) the research, specifically its quality, novelty, and significance of the contribution.</span></p>
<p dir="ltr"><span><span>Based on the results from the poster session, the judges will select students to advance to the second round. During the second round, students will have the opportunity to give a short presentation of their research (10 minutes) followed by a question and answer period (5 minutes), which will be evaluated by a panel of judges. Winners will be announced during the closing plenary.</span></span></p>

