<h3><a name="TOC-Message-from-the-Student-Game-Competition-Chairs"></a>Message from the Student Research Competition Chairs</h3>
<p dir="ltr"><span>The Student Research Competition (SRC) is a forum for undergraduate and graduate students to showcase their research, exchange ideas, and improve their communication skills while competing for prizes at CHI 2015. The CHI SRC competition is a branch of the </span><a href="http://www.acm.org/src"><span>ACM Student Research Competition</span></a><span> which hosts similar competitions at other ACM conferences.</span></p>
<p dir="ltr"><span>The Student Research Competition has the following goals:</span></p>
<ul><li dir="ltr">
<p dir="ltr"><span>to give undergraduate and graduate students the opportunity to share their research ideas and results at CHI in a special forum that provides visibility for their work</span></p>
</li>
<li dir="ltr">
<p dir="ltr"><span>to give students the opportunity to meet with and interact with CHI attendees to share ideas, gain new insights, and understand possible practical applications</span></p>
</li>
<li dir="ltr">
<p dir="ltr"><span>to give students an opportunity to sharpen their communication skills</span></p>
</li>
<li dir="ltr">
<p dir="ltr"><span>to provide detailed feedback to students about their research and presentation, from a panel of distinguished judges from industry and academia</span></p>
</li>
<li dir="ltr">
<p dir="ltr"><span>to recognize and reward outstanding student research</span></p>
</li></ul>
<p dir="ltr"><span>Participants must be students pursuing an academic degree at the time of initial submission. The contest has two categories, one for undergraduate research and the other for graduate research. Three winners will be selected in each category. Research completed while the student was an undergraduate may be submitted to the undergraduate category even if the student is now a first-year graduate student.</span></p>
<p dir="ltr"><span>Each competition entry must be authored by one student only - neither supervisors or other students are allowed as co-authors.</span></p>
<p dir="ltr"><span>For work accepted to the CHI 2015 Student Research Competition,</span><span> </span><span>a travel grant of up to US $500 will be awarded to help cover travel expenses to the conference. While the student must be an ACM member to qualify for travel funding and awards, she/he does not need to be one to submit to the competition.</span></p>
<p dir="ltr"><span>The top three winners at CHI 2015 in each category (undergraduate and graduate) will receive prizes of US $500, US $300, and US $200, respectively. All winners will receive a medal and two-year complimentary ACM membership with a subscription to ACM's Digital Library. Winners will be recognized during the closing plenary session of the CHI 2015 conference. The first-place winners will also go on to compete in the </span><a href="http://www.acm.org/src/subpages/participate.html"><span>ACM grand finals</span></a><span> with winners from other ACM conferences.</span></p>

