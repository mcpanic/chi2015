<h3><span><span>Student Research Competition Selection Process</span></span></h3>
<p dir="ltr"><span>The Student Research Competition is a </span><a href="http://chi2015.acm.org/authors/call-for-participation#juried"><span>juried</span></a><span> track for CHI 2015. Juried content is reviewed by a jury of experts that will evaluate the work based on its overall quality, originality, and relevance to the CHI community. While not considered archival, Student Research Competition extended abstracts will be archived in the ACM Digital Library. Publishing in the Student Research Competition will not constrain future submissions as your abstract and poster are not considered to be a prior publication of the work for the purposes of a future CHI Paper or CHI Note or for a journal publication.</span></p>
<p dir="ltr"><span>Submissions will be evaluated based on:</span></p>
<ul><li dir="ltr">
<p dir="ltr"><span>Quality of work</span></p>
</li>
<li dir="ltr">
<p dir="ltr"><span>Novelty of approach</span></p>
</li>
<li dir="ltr">
<p dir="ltr"><span>Significance of the contribution to the field of HCI</span></p>
</li>
<li dir="ltr">
<p dir="ltr"><span>Clarity of written presentation</span></p>
</li>
<li dir="ltr">
<p dir="ltr"><span>Visual presentation (poster)</span></p>
</li></ul>
<p dir="ltr"><span><span>Submissions should not contain&nbsp;sensitive, private, or proprietary information that cannot be disclosed at publication time.&nbsp; <span>Submissions </span>should NOT be anonymous. However, confidentiality of submissions will be maintained during the review process. All rejected submissions will be kept confidential in perpetuity. All submitted materials for accepted submissions will be kept confidential until the start of the conference,<span><span><span>&nbsp;with the exception of title and author information which will be published on the website prior to the conference.&nbsp;</span></span></span></span></span></p>
<p dir="ltr"><span>Up to twenty-five students in total will be chosen to participate in the competition at CHI 2015.</span></p>
<p dir="ltr"><span>Submissions not accepted for the Student Research Competition may be invited to be part of the Works-in-Progress track.</span></p>
