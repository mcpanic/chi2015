<h3>Why Stay at the Official CHI Hotels?</h3>
<p>
ACM and CHI work hard to make sure that the conference rate is competitive, and guarantee the comfort, safety, and proximity to the COEX convention center of all official conference hotels, This year, we have seven hotels to choose from – ranging from inexpensive residence-style to international business hotels that are directly connected to COEX – to meet all price points.
</p>

<p>
The housing bureau site offers one-stop reservations for CHI attendees and accepts reservations online or by fax. Utilizing the housing bureau also allows us to keep our conference costs low and continue to offer room blocks for our attendees.
</p>

<p>
Plus, all official CHI 2015 hotels offer complimentary internet in your guest room. Attendees booking through any other mechanism will not receive complimentary internet.
</p>

<h3>Hotel Reservations</h3>
<p>Official CHI 2015 hotels will not accept CHI reservations directly. Reservations must be made through the CHI housing bureau link to <a href="http://chi2015.koconex.co.kr">housing site</a>. If you require group reservations or other special assistance, please contact the housing bureau directly.</p>
<p>Please note that a deposit of one night’s room and tax via credit card or wire transfer is required to reserve your a hotel room on the housing site. If you wish to make a group reservation or require special assistance, please contact the housing bureau for details.</p>

